#include <bits/stdc++.h>
using namespace std;
using i64 = int64_t;
using i128 = __int128_t;



void solve()
{
    int n;
    cin >> n;
    if (n == 0)
    {
        cout << "#\n";
        return;
    }

    int len = pow(3, n);
    vector<vector<char>> g(len, vector<char>(len));
    auto dfs = [&](auto dfs, int n, int x, int y)
    {
        if (n == 0) 
        {
            g[x][y] = '#';
            return;
        }
        int clen = pow(3, n - 1);
        for (int i = x + clen; i < x + 2 * clen; i++)
        {
            for (int j = y + clen; j < y + 2 * clen; j++)
            {
                g[i][j] = '.';
            }
        }

        for (int r = 0; r < 3; r++)
        {
            for (int c = 0; c < 3; c++)
            {
                if (r == 1 && c == 1) continue;
                dfs(dfs, n - 1, x + c * clen, y + r * clen);
            }
        }
    };
    dfs(dfs, n, 0, 0);
    for (int i = 0; i < len; i++)
    {
        for (int j = 0; j < len; j++)
        {
            cout << g[i][j];
        }
        cout << "\n";
    }
}

int main()
{
    ios::sync_with_stdio(false);
    cout.tie(nullptr);
    cin.tie(nullptr);
    int T = 1;
    // cin >> T;
    while (T--)
        solve();
    return 0;
}

