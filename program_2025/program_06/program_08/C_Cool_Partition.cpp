mt19937_64 rng(chrono::steady_clock::now().time_since_epoch().count());
auto rnd = [](uint l, uint r) { return (l <= r ? uniform_int_distribution<uint>(l, r)(rng) : 0); };



void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
        cin >> a[i];

    map<int, int> cur, nex;
    cur[a[0]] = 1;
    int ans = 1;
    for (int i = 1; i < n; i++)
    {
        nex[a[i]] = 1;
        if (cur.find(a[i]) != cur.end())
            cur.erase(a[i]);

        if (cur.size() == 0)
        {
            cur = nex;
            nex.clear();
            ans++;
        }
    }

    cout << ans << "\n";
}

