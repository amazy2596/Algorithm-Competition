void solve()
{
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
        if (i >= 2)
        {
            if (a[i - 2] == a[i - 1] && a[i - 1] == a[i])
            {
                cout << "Yes\n";
                return;
            }
        }
    }
    cout << "No\n";
}

