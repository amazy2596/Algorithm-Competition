void solve()
{
    int n;
    cin >> n;
    string s;
    cin >> s;

    int l = 0;
    while (l < n && s[l] == 'z')
        l++;

    int r = l;
    while (r < n && s[l] >= s[r + 1])
        r++;

    int dist = 'z' - s[l];
    for (int i = l; i <= r; i++)
        s[i] += dist;

    cout << s << "\n";
}

