void solve()
{
    int n, m;
    cin >> n >> m;

    int ans = 0;
    for (int k = 30; k >= 0; k--)
    {
        int x = 1ll << k;

        if (n / m >= x)
        {
            n -= m * x;
            ans += x;
        }
        else 
        {
            if (n / m >= x - 1)
            {
                n -= (n - (x - 1) * m + x - 1) / x * x;
            }
        }
    }

    cout << ans << "\n";
}

